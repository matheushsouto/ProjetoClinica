    private void jButtonSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSalvarActionPerformed
        if (jTextFieldNome.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Preencha o NOME");
            jTextFieldNome.requestFocus();
        } else if (jTextFieldCrm.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Preencha o CRM");
            jTextFieldCrm.requestFocus();
        } else if (flag == 1) {
            mod.setNome(jTextFieldNome.getText());
            mod.setEspecialidade((String) jComboBoxEspecialista.getSelectedItem());
            mod.setCrm(jTextFieldCrm.getText());
            control.Salvar(mod);
            //Limpando os campos
            jTextFieldNome.setText("");
            jTextFieldCrm.setText("");
            jTextFieldNome.setEnabled(false);
            jTextFieldCrm.setEnabled(false);
            jComboBoxEspecialista.setEnabled(false);
            jButtonSalvar.setEnabled(false);
            jButtonCancelar.setEnabled(false);
            try {
                //Atualizando a tabela
                preencherTabela("SELECT * FROM medicos ORDER BY nome_medico");
            } catch (SQLException ex) {
                Logger.getLogger(FormMedico.class.getName()).log(Level.SEVERE, null, ex);
            }
        } else {
            //Setando dados nos modelos
            mod.setCod((Integer.parseInt(jtCod.getText())));
            mod.setNome(jTextFieldNome.getText());
            mod.setEspecialidade((String) jComboBoxEspecialista.getSelectedItem());
            mod.setCrm(jTextFieldCrm.getText());

            control.Editar(mod);
            jtCod.setText("");
            jTextFieldNome.setText("");
            jTextFieldCrm.setText("");
            // Bloqueando campos
            jTextFieldNome.setEnabled(false);
            jTextFieldCrm.setEnabled(false);
            jComboBoxEspecialista.setEnabled(false);
            jButtonSalvar.setEnabled(false);
            jButtonNovo.setEnabled(true);
            jButtonCancelar.setEnabled(false);
            try {
                //Atualizando a tabela
                preencherTabela("SELECT * FROM medicos ORDER BY nome_medico");
            } catch (SQLException ex) {
                Logger.getLogger(FormMedico.class.getName()).log(Level.SEVERE, null, ex);
            }

        }
    }//GEN-LAST:event_jButtonSalvarActionPerformed

